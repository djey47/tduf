    private final String name;
    private final byte[] contents;
    private final byte[] normalizedContents;

    MaterialPiece(String name) {
        this(name, null);
    }

    MaterialPiece(String name, byte[] contents) {
        this.name = name;
        this.contents = contents;
        this.normalizedContents = normalizeContents(contents);
    }

    /**
     * @return material piece having provided binary contents as byte array
     */
    public static MaterialPiece fromBinaryContents(byte[] binaryContents) {
        return Stream.of(MaterialPiece.values())
                .filter(piece -> UNKNOWN != piece)
                .filter(piece -> Arrays.equals(piece.normalizedContents, binaryContents))
                .findFirst()
                .orElse(UNKNOWN);
    }

    public String getName() {
        return name;
    }

    private static byte[] normalizeContents(byte[] cSharpBinaryContents) {
        if (cSharpBinaryContents == null) {
            return null;
        }

        //byte[] clonedContents = copyOf(cSharpBinaryContents, cSharpBinaryContents.length);
        //ArrayUtils.reverse(clonedContents);
        return copyOf(cSharpBinaryContents, 64);
    }

    private static byte toSigned(int unsignedValue) {
        return (byte) (unsignedValue - 256);
    }

